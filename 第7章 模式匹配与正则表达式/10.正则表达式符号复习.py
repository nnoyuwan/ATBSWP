# ?匹配零次或一次前面的分组。
# *匹配零次或多次前面的分组
# +匹配一次或多次前面的分组
# {n}匹配n次前面的分组
# {n, }匹配n次或更多次前面的分组
# {, m}匹配0次到m次前面的分组
# {n, m}匹配n次到m次前面的分组
# {n, m}?或*?或+?对前面的分组进行非贪婪匹配
# ^spam匹配以spam开头的字符串
# spam$匹配以spam结尾的字符串
# .匹配除换行外的任意字符
# \d、\w和\s匹配数字、字符、和空格
# \D、\W、\S匹配除数字、字符、空格外的任意字符
# [abc]匹配括号内的任意字符
# [^abc]匹配不在括号内的任意字符
