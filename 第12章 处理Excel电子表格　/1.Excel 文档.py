# 件。例如，可能有一个无聊的任务，需要从一个电子表格拷
# 贝一些数据，粘贴到另一个电子表格中。或者可能需要从几
# 千行中挑选几行，根据某种条件稍作修改。或者需要查看几
# 百份部门预算电子表格，寻找其中的赤字。正是这种无聊无
# 脑的电子表格任务，可以通过 Python 来完成。


